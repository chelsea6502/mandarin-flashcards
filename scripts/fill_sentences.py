# Column indices (1-based for openpyxl)
COL = {"s1": 5, "s2": 7, "s3": 9}


def apply_sentences():
    wb = openpyxl.load_workbook(XLSX_PATH)
    ws = wb.active
    rows = list(ws.iter_rows(min_row=2, values_only=True))
    updated = 0
    for row_idx, fills in SENTENCES.items():
        if row_idx >= len(rows):
            print(f"WARNING: row_idx {row_idx} out of range", file=sys.stderr)
            continue
        row = rows[row_idx]
        excel_row = row_idx + 2
        for col_key, value in fills.items():
            col_num = COL[col_key]
            existing = row[col_num - 1]
            if existing:
                continue  # never overwrite
            ws.cell(row=excel_row, column=col_num, value=value)
            updated += 1
    wb.save(XLSX_PATH)
    print(f"Updated {updated} cells in {XLSX_PATH}")


if __name__ == "__main__":
    apply_sentences()
